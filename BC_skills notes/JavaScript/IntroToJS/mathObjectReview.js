// MATH OBJECT REVIEW
// Math object contains properties (Set values) and methods (Changes the value)

// Syntax for using the Math object
// Math.propertyName or Math.methodName()

// Example of a Math object property
// Returns the value of PI (A.K.A. 3.14)
Math.PI;

// Example of a Math object method that takes an argument
// Returns the absolute (positive) value of the number entered
Math.abs(-4);

// Example of a Math object method that does NOT take an argument
// Returns the absolute (positive) value of the number entered
Math.random();