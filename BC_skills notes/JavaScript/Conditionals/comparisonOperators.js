// COMPARISON OPERATORS

// Greater Than (Example: >)
console.log(3 > 1);
console.log(0.9 > 1);

// Less Than (Example: <)
console.log(3 < 1);
console.log(0.9 < 1);

// Greater Than or Equal To (Example: >=)
console.log(3 >= 3);
console.log(3 >= 2);

// Less Than or Equal To (Example: <=)
console.log(3 <= 3);
console.log(3 <= 2);

// Equal Comparisons
// Double Equals (Example: ==)
console.log(3 == 3);
console.log(3 == 2);
console.log(3 == "3");

// Triple Equals (A.K.A. Strict Equality (Example: ===))
// Checks if both values are the same data type
console.log(3 === "3");
console.log(3 === 3);

// Not Equal Comparisons
// Single not equals (Example: !=)
// Checks to see if values are NOT equal to each other
console.log(3 != 3);
console.log(3 != 2);

// Double not equals (Example: !==)
// Checks to see if values are NOT equal to each other
// Checks if both values are NOT the same data type
console.log(3 !== 3);
console.log(3 !== "3");