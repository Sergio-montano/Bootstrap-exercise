// ARRAYS
// An Array is a data structure that can store multiples values

// This is an empty array
let cryptids = [];

// This is an array of strings
cryptids = ["Bigfoot", "Loch Ness Monster", "Yeti"];
console.log(cryptids);

// Length (Returns the amount of items in an array)
// String Example
console.log("Moth Man".length);
// Array Example
console.log(cryptids.length);

// Index (Returns a specific item in an array)
// String Example
console.log("Jersey Devil"[1]);
// Array Example
console.log(cryptids[1]);

// Changing an item in an array using index
cryptids[2] = "Abominable Snowman";
console.log(cryptids);

// Adding an item to an array using index
cryptids[cryptids.length] = "Chupacabra";
console.log(cryptids);

